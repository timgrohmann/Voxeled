package Models;

public class GUITexturedVertex {
    public final float x;
    public final float y;
    public final float u;
    public final float v;

    public GUITexturedVertex(float x, float y, float u, float v) {
        this.x = x;
        this.y = y;
        this.u = u;
        this.v = v;
    }
}
